# Tuple
# Ordered collection
# Immutable (cannot be changed after creation)
# Allows duplicates

t = (1, 2, 3)
print(t)


# Creating a Tuple
t1 = (10, 20, 30)
t2 = 10, 20, 30   # also valid

print(t1)
print(t2)


# Count and Index Methods

# count()
t = (1, 2, 2, 3)

print(t.count(2))


# index()
t = (10, 20, 30)

print(t.index(20))


